    <p>En esta sección&nbsp;se encuentra información sobre la gestión realizada por parte de la Secretaría Distrital de Movilidad, como líder del sector , descrita en acciones, estrategias y proyectos concertados en el marco de la Política Pública de Mujer y Equidad de Género, que están orientadas a la garantía de derechos de las mujeres y al logro de la equidad de género al interior de la entidad, bajo la misionalidad de la SDM.</p>
    <br>

    <hr>
    <h4><strong>Ruta Única&nbsp;de Atención a Mujeres Víctimas de Violencias de Bogotá</strong></h4>

    <p>Bogotá cuenta con la Ruta Única de Atención a mujeres víctimas de violencias, a través de la cual las mujeres víctimas y la ciudadanía en general, pueden informarse sobre a dónde acudir en casos de violencias de género, cómo y dónde solicitar orientación, atención en salud, medidas de protección o cómo acceder efectivamente a la justicia.&nbsp;<em>(Acuerdo 676 de 2017 del Concejo de Bogotá).&nbsp;</em>Haga clic sobre la imagen para ir al sitio web.&nbsp;&nbsp;</p>

