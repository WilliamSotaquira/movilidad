    <p>Bogotá cuenta con la Ruta Única de Atención a mujeres víctimas de violencias, a través de la cual las mujeres víctimas y la ciudadanía en general, pueden informarse sobre a dónde acudir en casos de violencias de género, cómo y dónde solicitar orientación, atención en salud, medidas de protección o cómo acceder efectivamente a la justicia. <em>(Acuerdo 676 de 2017 del Concejo de Bogotá). </em>Haga clic sobre la imagen para ir al sitio web. </p>
