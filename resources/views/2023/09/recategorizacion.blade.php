	<div class="row">
		<div class="col-sm-12">
			<h3>Preguntas y respuestas frecuentes - Migrantes</h3>

			<h4>Proyecto de recategorización de licencias de conducción B1 – C1 y C1 a C2 para refugiados y migrantes</h4>

			<p>La Secretaría Distrital de Movilidad, con el apoyo del Banco Interamericano de Desarrollo (BID) y el Gobierno de Canadá, invita a los refugiados y migrantes, interesados en conseguir licencias de conducción C1 y C2, que les permitan conducir vehículos públicos pequeños y medianos en Colombia, a participar en el proyecto de recategorización de licencias de conducción en Bogotá.</p>

			<p>Esta convocatoria se hace en colaboración del Grupo Interagencial de Flujos Migratorios Mixtos - GIFMM.</p>

			<p>La convocatoria estará abierta desde el 25 de septiembre de 2023.</p>

			<dl>
				<dt>1. ¿Quiénes pueden participar en la convocatoria?</dt>
				<dd><strong>R. </strong>La convocatoria va dirigida a personas migrantes (mujeres, hombres y personas de géneros diversos), que cumplan los siguientes requisitos:</dd>
				<dd>
					<ul>
						<li>Ser mayor de 18 años.</li>
						<li>Tener el Permiso de Protección Temporal (PPT) físico o cédula de extranjería, en el caso de las personas de nacionalidad venezolana.</li>
						<li>Tener el pasaporte o cédula de extranjería para las personas de otras nacionalidades diferentes a la venezolana.</li>
						<li>Saber conducir, preferiblemente vehículos medianos y grandes.</li>
						<li>Residir en Bogotá o tener la intención de vivir en la ciudad, por lo menos durante los 12 meses subsiguientes.</li>
					</ul>
				</dd>
			</dl>

			<dl>
				<dt>2. ¿Cuántas personas migrantes serán beneficiadas por el proyecto?</dt>
				<dd><strong>R. </strong>Se seleccionarán un total de 120 personas migrantes, como beneficiarias del proyecto de recategorización de licencias.</dd>
			</dl>

			<dl>
				<dt>3. ¿Qué apoyos recibiré si soy seleccionado como beneficiario?</dt>
				<dd><strong>R. </strong>Las personas que sean seleccionadas, en el marco del proyecto, recibirán lo siguiente:</dd>
				<dd>
					<ul>
						<li>Exámenes de aptitud en conducción.</li>
						<li>Curso de recategorización/expedición de licencia de conducción C1.</li>
						<li>Exámenes de aptitud en conducción (cada recategorización es un examen).</li>
						<li>Curso de recategorización de licencia de conducción de C1 a C2.</li>
						<li>Pago de los derechos ante la Secretaría Distrital de Movilidad para la expedición de las licencias C1 y C2.</li>
						<li>Una recarga en la tarjeta SITP, equivalente a 20 trayectos por mes, durante el proceso de formación.</li>
					</ul>
				</dd>
			</dl>

			<dl>
				<dt>4. ¿Con esta convocatoria me conseguirán un empleo como conductor en Bogotá?</dt>
				<dd><strong>R. </strong>No. El proyecto no garantiza el acceso a una vacante de empleo. La convocatoria se enfoca en apoyar a aquellas personas que deseen conseguir las licencias de conducción C1 y C2, que les permita trabajar como operarias de transporte privado o público en cualquier ciudad del país; sin embargo, una vez se termine el proyecto de recategorización de licencias, se contempla la posibilidad de remitir algunas hojas de vida a operadores del transporte público en Bogotá.</dd>
			</dl>

			<dl>
				<dt>5. ¿Cuánto tiempo durará el proyecto?</dt>
				<dd><strong>R. </strong>El proyecto durará aproximadamente 6 meses. Por tanto, las personas beneficiarias deberán contar con disponibilidad para realizar los cursos y los trámites respectivos durante ese periodo.</dd>
			</dl>

			<dl>
				<dt>6. Yo trabajo y tengo poca disponibilidad de tiempo ¿Necesito dedicar muchas horas a los cursos de formación? ¿Debo cumplir un horario para realizar los cursos de recategorización de licencias?</dt>
				<dd><strong>R. </strong>No. Los horarios de los cursos no son intensivos y son ajustables. Lo importante es que la persona beneficiaria cumpla el número de horas teóricas y prácticas que estipula el Centro de Enseñanza Automovilística para pasar de una licencia B1 a C1 (45 horas) y de una licencia C1 a C2 (45 horas). La persona beneficiaria podrá organizar su horario para tomar las sesiones de aprendizaje, incluso los fines de semana.</dd>
			</dl>

			<dl>
				<dt>7. ¿Puedo inscribirme si no tengo el PPT físico o aprobado, pero tengo un certificado en trámite del PPT?</dt>
				<dd><strong>R. </strong>No. El certificado de trámite del PPT no es un documento válido para participar en esta convocatoria.</dd>
			</dl>

			<dl>
				<dt>8. Tengo una licencia de conducción para manejar moto, ¿puedo participar en la convocatoria?</dt>
				<dd><strong>R. </strong>No. Sólo pueden participar aquellas personas que tengan una licencia de conducción adquirida en su país o en Colombia para manejar automóviles, microbuses, buses o camiones.</dd>
			</dl>

			<dl>
				<dt>9. ¿Es necesario tener experiencia como conductor de transporte público o privado para participar en esta convocatoria?</dt>
				<dd><strong>R. </strong>Si. Es necesario tener experiencia como conductor de transporte, como mínimo privado, en su país o en Colombia.</dd>
			</dl>

			<dl>
				<dt>10. ¿La convocatoria sólo aplica para personas que vivan en Bogotá?</dt>
				<dd><strong>R. </strong>Sí. La convocatoria está dirigida a personas que residan en Bogotá o que quieran radicarse en la ciudad por al menos 6 meses.</dd>
			</dl>

			<dl>
				<dt>11. ¿Me pedirán dinero para participar en el proyecto?</dt>
				<dd><strong>R. </strong>No. La participación en el proyecto es totalmente gratis. Sugerimos denunciar en el siguiente <a href="https://www.movilidadbogota.gov.co/web/formulario_de_denuncias" target="_blank">enlace</a>, si alguna persona inescrupulosa le indica que debe pagar para participar o ser seleccionado en el proyecto.</dd>
			</dl>

			<dl>
				<dt>12. ¿Dónde me inscribo?</dt>
				<dd><strong>R. </strong>Les invitamos a inscribirse en el <a href="https://forms.gle/diwTSPkdkVQFXbGW8" target="_blank">enlace</a>, si cumple con los requisitos expuestos en la pregunta frecuente número 1.</dd>
			</dl>

			<dl>
				<dt>13. ¿Qué sigue después de la inscripción?</dt>
				<dd><strong>R. </strong>Las personas inscritas serán contactadas para verificar el cumplimiento de la documentación requerida (PPT, licencia de conducción adquirida en Venezuela o en Colombia). Verificada la documentación, una vez preseleccionados, ingresarán a un proceso de selección final por parte de expertos del proyecto.</dd>
			</dl>

			<dl>
				<dt>14. ¿Cómo sabré si fui seleccionado(a) para el proyecto?</dt>
				<dd><strong>R. </strong>Las personas beneficiarias serán contactadas por correo o por teléfono. Si no son contactadas en el transcurso de los siguientes 30 días después de la inscripción y el haber pasado por el proceso de selección, se entenderá que no fueron beneficiadas en esta oportunidad.</dd>
			</dl>
