                              <div class="text-description">
                                  <h4>El Plan Maestro es un instrumento de planeamiento urbanístico que desarrolla y complementa el Plan de Ordenamiento Territorial, con el objetivo de lograr una movilidad sostenible, segura, equitativa e incluyente.</h4>
                              </div>
                          </div>
                      </div>
                  </div>
                  <div role="tabpanel" class="tab-pane fade" id="tab2">
                      <div class="row">
                          <div class="col-xs-12 col-sm-6 col-sm-offset-6">
                              <div class="text-description">
                                  <div>
                                      <h4>El Plan sirve de guía para definir las políticas, objetivos, estrategias y las metas de largo, mediano y corto plazo, mediante la formulación de proyectos, sus cronogramas, y mecanismos de evaluación y ajuste.</h4>
                                      <br>
                                      <h4>También sirve para darle prioridad a los modos de transporte sostenibles y articular los sistemas de movilidad con la estructura urbana.</h4>

                                  </div>
                              </div>
                          </div>
                      </div>
                  </div>
                  <div role="tabpanel" class="tab-pane fade" id="tab3">
                      <div class="row">
                          <div class="col-xs-12 col-sm-6 col-sm-offset-6">
                              <div class="text-description">
                                  <div>
                                      <h4>El Plan de Movilidad tiene una vigencia de 12 años, es decir, tres periodos de gobierno, que corresponde con la vigencia del POT. Los proyectos del Plan de Movilidad se formulan con metas para el corto (4 años), mediano (8 años) y largo plazo (12 años). </h4>
                                      <br>
                                      <h4>Adicionalmente, cada administración distrital formula el Plan Distrital de Desarrollo como un instrumento de gobierno de corto plazo (4 años) que prioriza las inversiones para materializar los proyectos definidos en el POT y el PMM"</h4>
