                          <blockquote>
                              <p class="rtejustify">En el Plan Maestro de Movilidad, PMM, se formulan las determinaciones generales y normativas del Sistema Movilidad, el cual tiene en cuenta la interdependecia que establece la estrategia de ordenamiento para el Distrito Capital.</p>
                              <p class="rtejustify">El PMM es un proyecto de ciudad-región, orientado, fundamentalmente, a mejorar su productividad sectorial y a aumentar su competitividad. Los planes maestros constituyen el instrumento de planificación fundamental en el marco de la estrategia de ordenamiento; permiten definir las necesidades de generación de suelo urbanizado de acuerdo con las previsiones de crecimiento poblacional y de localización de la actividad económica, para programar los proyectos de inversión sectorial en el corto, mediano y largo plazo.&nbsp;</p>
