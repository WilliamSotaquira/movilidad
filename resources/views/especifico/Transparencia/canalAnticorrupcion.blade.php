<div class="row rr">
    <div class="col-xs-12">
        <h1 class="h2-cc">¿Qué se entiende por corrupción?</h1>
    </div>
</div>
<div class="row rg">
    <div class="col-xs-12">
        <p class="p-cc">Se percibe como <strong>“el uso del poder para desviar la gestión de lo público hacia el beneficio privado, lo anterior se traduce en que las prácticas corruptas pueden ser realizadas tanto por actores públicos como por actores privados, siempre y cuando estos tengan poder e incidencia en la toma de decisiones y en la administración y provisión de los bienes públicos; en otras palabras, se entiende como la “acción y efecto de corroer”.</strong></p>
        <p class="p-cc"><strong>Fuente: </strong><small> (2013) CONPES 167 “Estrategia Nacional de la Política Integral Anticorrupción”, pág. 12.</small></p>
    </div>
</div>

<div class="row ry">
    <row class="col-xs-12">
        <h1 class="h2-cc">¿Qué es una denuncia?</h1>
    </row>
</div>
<div class="row rg">
    <div class="col-xs-12">
        <p class="p-cc">Es el relato que un ciudadano realiza, en cumplimiento de su deber de solidaridad, para enterar a las autoridades de la existencia de hechos irregulares, con el ﬁn de activar los mecanismos de investigación y sanción.</p>
    </div>
</div>
<div class="row rr">
    <div class="col-xs-12">
        <h1 class="h2-cc">Deﬁnición de términos a la hora de denunciar posibles casos de corrupción</h1>
    </div>
</div>
<div class="row rg">
    <div class="col-xs-12">
        <p class="p-cc"><strong class="stR">Corrupción:</strong> uso del poder para desviar la gestión de lo público hacia el beneﬁcio.</p>
        <p class="p-cc"><strong class="stR">Soborno:</strong> es la oferta, promesa, entrega, aceptación o solicitud, por parte de un colaborador de la Secretaría Distrital de Movilidad, o de un externo, de una ventaja indebida de cualquier valor (que puede ser de naturaleza ﬁnanciera o no ﬁnanciera), directamente o indirectamente, e independiente de su ubicación, como incentivo o recompensa para que un funcionario o contratista de la Entidad, actúe o deje de actuar en relación con el desempeño de las obligaciones.</p>
    </div>
</div>
<div class="row rimg">
    <img src="https://www.movilidadbogota.gov.co/web/sites/default/files/Paginas/13-06-2022/anticorrupcion_c.webp" alt="" class="img-responsive w-100">
</div>
<div class="row rg">
    <div class="col-xs-12">
        <p class="p-cc"><strong class="stR">Inhabilidad:</strong> circunstancias creadas por la Constitución o la Ley que impiden o imposibilitan que una persona sea elegida o designada en un cargo público, y en ciertos casos, impiden el ejercicio del empleo a quienes ya se encuentren vinculados al servicio, y tienen como objetivo primordial, lograr la moralización, idoneidad, probidad e imparcialidad de quienes van a ingresar o ya están desempeñando empleos públicos.</p>
        <p class="p-cc"><strong class="stR">Incompatibilidad:</strong> imposibilidad jurídica de coexistencia de dos actividades.</p>
        <p class="p-cc"><strong class="stR">Conﬂicto de intereses:</strong> cuando el interés general propio de la función pública entra en conﬂicto con el interés particular y directo del servidor público.</p>
    </div>
</div>
<div class="row rimg">
    <img src="https://www.movilidadbogota.gov.co/web/sites/default/files/Paginas/13-06-2022/anticorrupcion_b.webp" alt="" class="img-responsive w-100">
</div>
<div class="row rg">
    <div class="col-xs-12">
        <p class="p-cc"><strong class="stR">Denuncia hechos de Corrupción:</strong> mecanismo implementado para que los ciudadanos, usuarios internos y/o externos puedan denunciar los hechos relacionados con presuntos actos de corrupción, entre ellos el soborno que tiene establecido dentro del Sistema Integrado de Gestión su propio protocolo de denuncias; la existencia de inhabilidades, incompatibilidades o conﬂicto de interés, así como aportar el material probatorio que sustenta la denuncia, siendo un lineamiento para preservar y fortalecer la transparencia en la Secretaría Distrital de Movilidad.</p>
        <p class="p-cc">Es importante tener en cuenta que las denuncias deben tener como mínimo la siguiente información, para poder encausar la investigación por parte de la entidad:</p>
        <ul class="ul-cc">
            <li>Una relación clara, detallada y precisa de los hechos de los cuales tiene conocimiento.</li>
            <li>En lo posible, exprese cómo ocurrieron los hechos, dónde y cuándo.</li>
            <li>Señalar el o los presuntos responsables o las señales particulares que permitan individualizarlos.</li>
            <li>Si prefiere puede suministrar su nombre y dirección de residencia, teléfono y correo electrónico, para contactarlo en el evento de ser necesario o para mantenerlo informado del curso de su denuncia.</li>
            <li>En el evento de preferir presentar la denuncia de forma anónima, asegúrese de que ésta amerite credibilidad y acompáñela de evidencias que permitan orientar la investigación.</li>
            <li>Informe si los hechos han sido puestos en conocimiento de otra autoridad, indique cuál.</li>
        </ul>
    </div>
</div>
