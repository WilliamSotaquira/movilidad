                <div class="panel-body">
                    <p><strong>Parágrafo.</strong> Sin perjuicio de la competencia del Alcalde Mayor de establecer y adoptar la organización interna y funcional de los organismos del Sector Central, la función de la Secretaría Distrital de Movilidad relacionada con ejecutar las políticas del sistema de movilidad en el componente de tránsito, para atender los requerimientos de organización, vigilancia y control del desplazamiento de pasajeros y de carga y de regulación y control del transporte público individual, transporte privado, transporte en bicicleta, motos y transporte de tracción animal será organizada como una dependencia interna de la Secretaría de Movilidad con autonomía administrativa y financiera.</p>
                </div>
            </div>
        </div>
    </div>
    <div class="row">
        <p>La dependencia interna con autonomía administrativa y financiera tendrá, entre otras, las siguientes funciones, bajo la orientación del Secretario de Movilidad:</p>
        <ul>
            <li>Vigilar el cumplimiento de las normas de tránsito.</li>
            <li>Regular y vigilar el sistema de señalización y semaforización.</li>
            <li>Velar por el cumplimiento de las normas sobre registro de vehículos automotores.</li>
            <li>Tramitar la expedición de licencias de conducción y solicitar la regulación para el funcionamiento de escuelas de enseñanza automovilística.</li>
            <li>Regular y controlar el transporte público individual.</li>
            <li>Regular y controlar las modalidades de transporte no motorizados y peatonal. Adelantar campañas de seguridad vial.</li>
            <li>Asumir las funciones reguladoras y de control que sean transferidas al Distrito Capital por el Gobierno Nacional en materia de tránsito.</li>
            <li>Aplicar las medidas de control en cuanto a la regulación del parqueo público y el estacionamiento en vías y espacios públicos cumpliendo con lo establecido en el POT y en el Plan Maestro de Movilidad.</li>
        </ul>
