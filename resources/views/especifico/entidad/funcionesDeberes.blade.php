    <div class="row">
        <h3>La Secretaría Distrital de Movilidad tiene las siguientes funciones básicas:</h3>
        <ul>
            <li>Formular y orientar las políticas sobre la regulación y control del tránsito, el transporte público urbano en todas sus modalidades, la intermodalidad y el mejoramiento de las condiciones de movilidad y el desarrollo de infraestructura vial y de transporte.</li>
            <li>Fungir como autoridad de tránsito y transporte.</li>
            <li>Liderar y orientar las políticas para la formulación de los planes, programas y proyectos de construcción, mantenimiento y rehabilitación de la infraestructura vial y de transporte del Distrito Capital.</li>
            <li>Diseñar y establecer planes y programas de movilidad en el corto, mediano y largo plazo dentro del marco del Plan de Ordenamiento Territorial.</li>
            <li>Diseñar, establecer, ejecutar, regular y controlar, como autoridad de tránsito y de transporte, las políticas sobre el tránsito y el transporte en el Distrito Capital.</li>
            <li>Formular y orientar políticas sobre democratización del sistema de transporte masivo y público colectivo.</li>
            <li>Orientar, establecer y planear el servicio de Transporte Público Urbano, en todas sus modalidades, en el Distrito y su área de influencia.</li>
            <li>Participar en el diseño de la política y de los mecanismos de la construcción y explotación económica de las terminales de transporte de pasajeros, de carga y de transferencia.</li>
            <li>Planear, coordinar y controlar la operación, entre otros mecanismos de seguridad vial, de la semaforización y señalización de los segmentos viales del Distrito Capital.</li>
            <li>Participar en la elaboración, regulación y ejecución del Plan de Ordenamiento Territorial; en la articulación del Distrito Capital con el ámbito regional para la formulación de las políticas y planes de desarrollo conjuntos, y en las políticas y planes de desarrollo urbano del Distrito Capital. Diseñar, establecer y ejecutar los planes y programas en materia de educación vial.</li>
            <li>Controlar, de conformidad con la normativa aplicable, el transporte intermunicipal en la jurisdicción del Distrito Capital.</li>
            <li>Administrar los Sistemas de información del sector.</li>
        </ul>
    </div>
