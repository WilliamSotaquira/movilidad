            <p>Acuerdo 257 de 2006: Artículo 108. Naturaleza, objeto y funciones básicas de la Secretaría Distrital de Movilidad. La Secretaría Distrital de Movilidad es un organismo del Sector Central con autonomía administrativa y financiera que tiene por objeto orientar y liderar la formulación de las políticas del sistema de movilidad para atender los requerimientos de desplazamiento de pasajeros y de carga en la zona urbana, tanto vehicular como peatonal y de su expansión en el área rural del Distrito Capital en el marco de la interconexión del Distrito Capital con la red de ciudades de la región central, con el país y con el exterior.</p>
