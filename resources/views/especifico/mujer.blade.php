                <h3 class="text-h3">30. ¿Por qué emplear mujeres en el SITP en los roles de conductoras?</h3>
                <p class="text-p">El sector transporte es un sector masculinizado y la mayor parte de la fuerza laboral en conducción y mantenimiento ha estado ocupada por hombres.</p>
                <p class="text-p">Desde la articulación intersectorial, interinstitucional, pública y privada, para el desarrollo de este proyecto, se contribuye con el cierre de brechas de género en el Distrito relacionadas con el acceso al empleo, considerando la mayor afectación del desempleo para las mujeres a raíz de la pandemia por el covid-19 y se convierte en un ejemplo de buenas prácticas frente a la garantía del derecho al trabajo de las mujeres en condiciones de igualdad y dignidad.</p>
                <p class="text-p">Además, desde la seguridad vial con perspectiva de género, se encontró que las mujeres son más prudentes, tienen mayor conciencia vial, son más respetuosas de los límites de velocidad y de distancias seguras entre vehículos y su responsabilidad en siniestros viales es menor que la masculina.</p>
                <p class="text-p">Así mismo, las mujeres son más puntuales y presentan menor cantidad de incidentes o conflictos con usuarios y usuarias del transporte público.</p>
