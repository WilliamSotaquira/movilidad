                <p class="text-p">Una vez se culmine el proceso de selección, antes de ser vinculadas al SITP las conductoras deberán presentar el Certificado de aprobación de la Evaluación de Aptitudes Físicas, Mentales y de Coordinación Motriz, expedido por profesionales de cada especialidad médica, pertenecientes a un Centro de Reconocimiento de Conductores (CRC) o a una Institución Prestadora de Salud (IPS) que cuenten con licencia o registro ante la respectiva autoridad competente. Esto, con el fin de verificar que la candidata examinada no tiene alguna enfermedad o discapacidad física o psicológica que pueda suponer una incapacidad o inhabilidad para conducir vehículos de transporte público de pasajeros.</p>
                <p class="text-p">Dicha evaluación médica, se debe realizar bajo los lineamientos establecidos en la normatividad vigente.</p>
                <p class="text-p">Cada prueba de evaluación debe ser realizada por el respectivo profesional de cada área, de acuerdo con su especialidad: medicina general o interna, oftalmología u optometría, fonoaudiología, sicología, y deberá contener como mínimo:</p>
                <p class="text-p">
                <ul>
                    <li class="text-li">Capacidad mental y de coordinación motriz.</li>
                    <li class="text-li">Capacidad de visión.</li>
                    <li class="text-li">Capacidad auditiva.</li>
                    <li class="text-li">Capacidad física general.</li>
                    <li class="text-li">Otros diagnósticos.</li>
                </ul>
                </p>
                <p class="text-p">El Certificado de Evaluación presentado debe evidenciar que la candidata cumple positivamente con los requisitos mínimos establecidos y deberá contener los resultados, parciales y consolidados de las exploraciones y valoraciones efectuadas para medir la capacidad de visión, capacidad auditiva, capacidad mental y de coordinación motriz y capacidad física general.</p>
                <p class="text-p">Toda situación de restricción y/o limitación debe ser registrada en el respectivo Certificado, el cual incluye:</p>
                <p class="text-p">
                <ul>
                    <li class="text-li">Limitaciones que pueden ser superadas con el uso de elementos específicos.</li>
                    <li class="text-li">Personas en condiciones de discapacidad, deben demostrar mediante la evaluación completa que se encuentran capacitadas para conducir con dicha limitación; así mismo, se debe dejar el registro de si para su labor requiere del uso de instrumentos ortopédicos o ayudas mecánicas, en función de la discapacidad particular.</li>
                    <li class="text-li">Limitaciones físicas progresivas, se deberá especificar en el informe y en el Certificado, la vigencia máxima del mismo y la fecha a partir de la cual la interesada deberá someterse a la práctica de una nueva evaluación de aptitud.</li>
