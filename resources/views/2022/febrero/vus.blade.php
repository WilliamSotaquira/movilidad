        <div class="parrafos">
            <dl>
                <dt>¿Qué es la Ventanilla Única de Servicios?</dt>
                <dd><strong>R. </strong>Es un espacio moderno, ágil y seguro operado por el Consorcio Circulemos Digital para la atención de la ciudadanía interesada en trámites y servicios de movilidad, implementada a través de una plataforma tecnológica y de puntos de atención presenciales distribuidos en diferentes localidades de Bogotá. Esta ventanilla le permite al usuario acceder ya sea de manera virtual o presencial a información, trámites y servicios de movilidad.</dd>
            </dl>
            <dl>
                <dt>¿Quién está a cargo de la Ventanilla Única de Servicios?</dt>
                <dd><strong>R. </strong>Está a cargo el Consorcio Circulemos Digital integrado por las empresas Data Tools S.A. (45.00%), Olimpia IT S.A.S. (40.00%) y Soluciones en Red S.A.S. (15.00%). El consorcio fue el ganador de la adjudicación de la Concesión de Trámites y Servicios de Movilidad realizada por la Secretaría Distrital de Movilidad.</dd>
            </dl>
            <dl>
                <dt>¿Cómo nació la Ventanilla Única de Servicios?</dt>
                <dd><strong>R. </strong>La Secretaría Distrital de Movilidad inició el proceso de estructuración en mayo de 2020, conformando un grupo de expertos para su planificación y organización. La noticia de su adjudicación se dio en noviembre del año 2021 dando como ganador al Consorcio Circulemos Digital.</dd>
                <dd>La Ventanilla Única de Servicios es la tercera concesión de trámites y servicios adjudicada en Bogotá, siendo la sucesora de Servicios Integrales para la Movilidad (SIM) (2007-2022) y de Servicios Especiales de Tránsito y Transporte (SETT) (1997 - 2007).</dd>
            </dl>
            <dl>
                <dt>¿Cuánto tiempo tendrá la concesión para la ejecución de este proyecto?</dt>
                <dd><strong>R. </strong>Está contemplado por un periodo de 10 años y tres meses, teniendo en cuenta las siguientes etapas:</dd>
                <dd>
                    <ul>
                        <li><strong>Etapa pre-operativa (3 meses aproximadamente):</strong> preparación para asumir la operación. Es el período de alistamiento durante el cual no hay remuneración y el operador actual mantiene su operación.</li>
                        <li><strong>Etapa de transición (un año):</strong> inicio de prestación del servicio.</li>
                        <li><strong>Etapa de operación plena (9 años):</strong> prestación del servicio.</li>
                    </ul>
                </dd>
            </dl>
