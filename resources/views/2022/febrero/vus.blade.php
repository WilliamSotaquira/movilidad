                <dt>¿La Ventanilla Única de Servicios me permite hacer trámites en línea?</dt>
                <dd><strong>R. </strong>Sí, la virtualización de los trámites y servicios de movilidad es la base fundamental de la Ventanilla Única de Servicios, con la cual se da una mayor cobertura y acceso directo a la oferta de trámites y servicios en línea a través de dispositivos móviles, esta opción se dará próximamente en trámites que no requieren presencialidad de acuerdo con las exigencias del Ministerio de Transporte.</dd>
            </dl>
