        <div class='row'>
            <h3 class="text-center">Abecé del Pico y Placa 2022 en Bogotá</h3>
            <hr>
            <div class='col-xs-12 col-sm-12 col-md-12'>
                <dl>
                    <dt>¿En qué cambia el Pico y Placa en Bogotá?</dt>
                    <dd>
                        <p>El Pico y Placa tiene dos cambios importantes para la ciudadanía:</p>
                        <ol>
                            <li>Amplía el horario de restricción para los vehículos particulares en días hábiles desde las 6:00 a.m. hasta las 9:00 p.m. de acuerdo con el número de la placa.</li>
                            <li>Amplía la restricción a los nueve corredores de ingreso a Bogotá los días de retorno a la ciudad en puentes festivos distribuyendo la entrada entre placas pares e impares después del mediodía.</li>
                        </ol>
                    </dd>
                </dl>
                <dl>
                    <dt>¿A partir de cuándo se tienen contemplados estos cambios?</dt>
                    <dd>
                        <p>La restricción de circulación por Pico y Placa en días hábiles comienza a regir el martes 11 de enero de 2022.</p>
                        <p>El Pico y Placa Regional en los días de retorno en puentes festivos inicia en los nueve corredores de ingreso de la ciudad el lunes 10 de enero de 2022.</p>
                    </dd>
                </dl>
                <dl>
                    <dt>¿Cómo será el Pico y Placa en días hábiles?</dt>
                    <dd>El funcionamiento por número de placa par o impar es igual. La restricción aplica para las placas pares los días pares y para las impares los días impares.</dd>
                </dl>
                <dl>
                    <dt>¿Cómo funcionará el Pico y Placa Regional?</dt>
                    <dd>El Pico y Placa Regional es similar al que funciona en el municipio de Soacha sobre la autopista sur. Hasta el mediodía del retorno en los puentes festivos no hay restricción. A partir de las 12:00 del mediodía se permite el ingreso a Bogotá de los vehículos con placas pares hasta las 4:00 p.m. y luego, de 4:00 p.m. a 8:00 p.m. se permite el ingreso de los vehículos con placas impares.</dd>
                </dl>
                <dl>
                    <dt>¿Cuáles son los nueve corredores donde aplica el Pico y Placa Regional?</dt>
                    <dd>
                        <p>Los corredores de ingreso donde aplica la medida de Pico y Placa Regional el último día de puentes festivos son:</p>
                        <ul>
                            <li>Autopista norte: desde el peaje Andes hasta el portal norte, sentido norte - sur</li>
                            <li>Autopista sur: desde el límite municipal de Soacha hasta la avenida Boyacá, sentido sur - norte</li>
                            <li>Avenida Centenario (calle 13): desde el río Bogotá hasta la avenida ciudad de Cali (avenida carrera 86), sentido occidente - oriente</li>
                            <li>Avenida calle 80: desde el puente de guadua hasta el portal 80, sentido occidente - oriente</li>
                            <li>Avenida carrera 7: desde la calle 245 hasta la calle 183, sentido norte - sur</li>
                            <li>Avenida Boyacá vía al Llano: desde el túnel Argelino Durán Quintero hasta la antigua vía al Llano, sentido sur - norte</li>
                            <li>Vía Suba Cota: desde el río Bogotá hasta la avenida calle 170, sentido norte - sur</li>
                            <li>Vía la Calera: desde el peaje Patios hasta la avenida carrera 7, sentido oriente - occidente</li>
                            <li>Vía a Choachí: desde la vía a Monserrate hasta la avenida circunvalar, sentido oriente – occidente</li>
                        </ul>
                    </dd>
                </dl>
                <dl>
                    <dt>¿Cuáles son las sanciones por incumplir el Pico y Placa? </dt>
                    <dd>
                        <p>Las sanciones por el incumplimiento de la medida de Pico y Placa están estipuladas en el Código Nacional de Tránsito para la infracción C.14: “Transitar por sitios restringidos o en horas prohibidas por la autoridad competente. Además, el vehículo será inmovilizado”. </p>
                        <p>La multa equivale en 2022 a $468.500.</p>
                    </dd>
                </dl>
                <dl>
                    <dt>¿Cuáles son las excepciones para el Pico y Placa diario?</dt>
                    <dd>
                        <p>Los vehículos exceptuados de la medida de Pico y Placa en Bogotá son:</p>
                        <ol>
                            <li>Vehículos eléctricos y de cero emisiones contaminantes (deben registrase en la página web de la Secretaría de Movilidad <a href="https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular" target="_blank" rel="noopener noreferrer">https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular</a> )</li>
                            <li>Caravana presidencial</li>
                            <li>Vehículos de servicio diplomático o consular</li>
                            <li>Carrozas fúnebres</li>
                            <li>Vehículos de organismos de seguridad del Estado</li>
                            <li>Vehículos de emergencia</li>
                            <li>Vehículos utilizados para el transporte de personas en condición de discapacidad</li>
                            <li>Vehículos de empresas de servicios públicos domiciliarios</li>
                            <li>Vehículos destinados al control de tráfico y grúas </li>
                            <li>Vehículos de control de emisiones y vertimientos</li>
                            <li>Motocicletas</li>
                            <li>Vehículos asignados por la Unidad Nacional de Protección</li>
                            <li>Vehículos de medios de comunicación</li>
                            <li>Vehículos de autoridades judiciales</li>
                            <li>Vehículos de transporte escolar propiedad de instituciones educativas</li>
                            <li>Vehículos tipo automóvil, campero o camioneta destinados a la enseñanza automovilística</li>
                            <li>Vehículos con 3 o más ocupantes (deben registrase en la página web de la Secretaría de Movilidad <a href="https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular" target="_blank" rel="noopener noreferrer">https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular</a>)</li>
                            <li>Vehículos híbridos (deben registrase en la página web de la Secretaría de Movilidad <a href="https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular" target="_blank" rel="noopener noreferrer">https://www.movilidadbogota.gov.co/web/registro_restriccion_vehicular</a>)</li>
                            <li>Vehículos con Pico y Placa Solidario de acuerdo con la vigencia (deben registrase cada semana en la página web de la Secretaría de Movilidad <a href="http://picoyplacasolidario.movilidadbogota.gov.co/" target="_blank" rel="noopener noreferrer">picoyplacasolidario.movilidadbogota.gov.co</a> )</li>
                        </ol>
                    </dd>
                </dl>
                <dl>
                    <dt>¿Qué sucede con la excepción para el personal de salud?</dt>
                    <dd>
                        <p>La excepción para el personal de la salud está vigente en Bogotá hasta cuando lo esté la emergencia sanitaria decretada por el Gobierno Nacional.</p>
                    </dd>
                </dl>
                <dl>
                    <dt>¿Qué sucede con las excepciones por Pico y Placa Solidario, carro compartido y para el personal de salud en el Pico y Placa Regional?</dt>
                    <dd>
                        <p>Para la medida de Pico y Placa Regional en Bogotá en retorno de puentes festivos en el ingreso a Bogotá, NO aplican las excepciones por 3 o más ocupantes, por vehículos de personal médico y tampoco aplica la excepción de "Pico y Placa Solidario".</p>
                    </dd>
                </dl>
                <dl>
                    <dt>¿La medida de Pico y Placa Regional para el ingreso a Bogotá fue concertada con los municipios aledaños?</dt>
                    <dd>Sí. La Alcaldesa de Bogotá y el Secretario Distrital de Movilidad se reunieron con el Gobernador de Cundinamarca y los alcaldes de los municipios aledaños para concertar la medida y agilizar el tránsito en los puentes festivos en beneficio de los usuarios.</dd>
                </dl>
                <dl>
                    <dt>¿Se hará monitoreo a la medida de Pico y Placa?</dt>
                    <dd>La Secretaría Distrital de Movilidad estará monitoreando constantemente el crecimiento del parque automotor de motocicletas y carros como insumo para la toma de decisiones.</dd>
                </dl>
