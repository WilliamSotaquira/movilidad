            <div class="tab-pane fade" id="alcance" role="tabpanel">
                <div class="box-tab">
                    <h4>Alcance del Sistema de Gestión SST</h4>

                    <div class="box-tab-img">
                        <div class="row">
                            <div class="col-xs-12 col-xs-offset-0 col-sm-8 col-sm-offset-2"><img alt="" class="img-responsive w-100" src="https://www.movilidadbogota.gov.co/web/sites/default/files/Paginas/14-12-2021/jr1_4421.webp"></div>
                        </div>

                        <div class="box-tab-i"><i>Foto: Secretaría Distrital de Movilidad</i></div>
                    </div>

                    <p>El Sistema Gestión de la Seguridad y Salud en el Trabajo SG - SST, aplica a todas las actividades de la Secretaría Distrital de Movilidad, todas sus sedes y centros de trabajo y todos sus trabajadores, independiente de su forma de, contratación o vinculación, incluyendo los contratistas y subcontratistas y las partes interesadas. De igual manera, cobija al personal que desarrolle Teletrabajo y Trabajo remoto.</p>
                    &nbsp;

                    <h4>Alcance de la Certificación de la Norma ISO 45001:2018</h4>

                    <p>El alcance para el Sistema de Gestión SST bajo la norma ISO 45001:2018, comprende la formulación de políticas e implementación de estrategias de movilidad multimodal, diseño e implementación de medidas de gestión de tránsito y control vial, procesos contravencionales y atención a trámites y servicios para la ciudad de Bogotá.&nbsp;</p>

                    <p>Aplica a los procesos administrativos y operativos de las sedes Calle 13 y Paloquemao, todos sus trabajadores, independiente de su forma de contratación o vinculación.</p>
