              <h3 class="t2">Súmate a la ‘Caravana de la Movilidad’</h3>
              <div class="newspaper">
                  <p class="pn">Prepárese porque si es vecino de la Localidad de Barrios Unidos este sábado 23 de octubre puede participar en la ‘Caravana de la Movilidad’, cuyo recorrido empezará a las 10 de la mañana en el parque La Araña (Calle 75 con Carrera 22) del barrio San Felipe, donde se realizarán la socialización del proyecto ‘Barrios Vitales’ y Metro, y pasará por los parques que tendrán actividades durante toda la jornada.</p>
                  <p class="pn">La Caravana pasará por el parque del Gimnasio Norte (Calle 63G con Carrera 26), del barrio Benjamín Herrera, donde se realizará campaña de pedagogía para el uso del espacio público.</p>
                  <p class="pn">Luego estará en Parque Modelo (Calle 68 con Carrera 56B), cerca de la Olímpica del barrio Popular Norte, a donde asistentes podrá llevar su bicicleta para hacer el ‘Registro Bici’ y participar en actividades de pedagogía vial.</p>
                  <p class="pn">Después llegará al parque La X (Carrera 60 con Calle 67 A) donde estará durante la jornada un punto de información de IDU sobre los proyectos en la localidad y la Troncal Carrera 68.</p>
                  <p class="pn">La última parada de la Caravana será en el parque San Fernando (Calle 72 con carrera 57 A), epicentro de la Feria de Servicios de ‘Movilidad al Barrio’ y lugar escogido para estar este sábado más cerca de los vecinos de Barrios Unidos. ‘Movilidad al Barrio’ cada día más cerca de la gente.</p>
