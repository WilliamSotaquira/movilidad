      <div class="row">
          <div class="col-xs-12">
              <p class="pn text-justify">Una gran noticia está llegando a todos los barrios de Bogotá, esperada por familias y vecinos. La Secretaria Distrital de Movilidad (SDM) en compañía de sus entidades adscritas, como el Instituto de Desarrollo Urbano (IDU), la Unidad de Mantenimiento Vial (UMV) y TransMilenio, y otras entidades del Distrito están acercando a los parques de la ciudad sus servicios a la ciudadanía.</p>
              <div class="newspaper">
                  <p class="pn">El pasado 28 de agosto, la iniciativa conocida como ‘Movilidad al Barrio’ estuvo en la localidad de Puente Aranda y este sábado 23 de octubre estará en la localidad de Barrios Unidos con su amplia Feria Servicios y su Caravana de Gerencia en Vía.</p>
                  <p class="pn">Esta iniciativa, liderada por la Secretaría de Movilidad, propone un diálogo ciudadano en el que las entidades que integran el Sector de Movilidad de Bogotá darán soluciones de manera articulada en asuntos de gestión, trámites y servicios a las necesidades más urgentes de los habitantes de cada barrio y localidad.</p>
                  <p class="pn">En la Feria los asistentes podrán realizar el registro de su bicicleta, gestionar servicios y trámites de tránsito y transporte relacionados con su vehículo, averiguar por el estado de comparendos a su nombre, acuerdos de pago, traspasos y vehículos inmovilizados, entre otros. </p>
                  <p class="pn">También podrá acceder a la personalización de tarjetas de TransMilenio; el IDU brindará información sobre el proyecto Troncal 68, el proyecto Metro y tendrá un punto de atención al ciudadano; por su parte, la Unidad de Mantenimiento Vial llevará la información sobre las vías intervenidas y sus planes de trabajo. A través de Gerencia en Vía se abordará el manejo del tránsito, semaforización, pedagogía y señalización de la localidad.</p>
                  <p class="pn">Este evento cuenta con el apoyo de la Alcaldía Local de Barrios Unidos, los Gestores de Convivencia y la Policía Metropolitana de Bogotá.</p>
                  <p class="pn">Si necesita ponerse al día con un trámite o servicio de movilidad busque sus documentos porque el Sector Movilidad estará en el parque San Fernando (Calle 72 - Carrera 57 A), entre las 10:00 a.m. y las 4:00 p.m.</p>
                  <p class="pn">En esta versión también estará presente la Secretaría Distrital de Hacienda con su portafolio de trámites y servicios.</p>
                  <p class="pn">Además tendremos actividades lúdico pedagógicas para la comunidad y para que todos los miembros de la familia se sumen a este evento pues tendremos música, juegos, teatro y zumba, entre otras actividades.</p>
