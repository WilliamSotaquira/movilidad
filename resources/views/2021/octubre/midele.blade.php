                        <h4>Descripción</h4>
                        <p>¡Mídele la congestión a Bogotá! Invita a las organizaciones, empresas, universidades y en general, todos aquellos actores externos a la Secretaría Distrital de Movilidad (SDM) que se encuentren interesados en poner a prueba soluciones tecnológicas para la gestión de cobros por externalidades mediante el seguimiento y medición de los viajes.</p>
                        <p>Con la planeación e implementación de pilotos se busca que los participantes pongan a prueba desarrollos tecnológicos que faciliten la medición de unidades asociadas al cobro por congestión: distancia, tiempo, entrada en zonas de congestión, velocidad de circulación, ocupación, entre otras. Y posibles tecnologías que permitan hacer las veces de control de los vehículos que ingresan a zonas junto con la validación de las características de los mismos.</p>
                        <p>Las organizaciones seleccionadas tienen la posibilidad de poner a prueba sus soluciones tecnológicas apoyándose en el equipo técnico de la SDM y con información real del comportamiento en una ciudad que presenta múltiples retos a nivel Latinoamérica.</p>
                        <p>Igualmente, las organizaciones seleccionadas contarán con el acompañamiento de la ciudad para divulgar el trabajo y los resultados del piloto en Bogotá.</p>
