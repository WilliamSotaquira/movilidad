    <div class="seccion-1">
        <div class="encabezado-seccion">
            <h4>NUESTRA HOJA DE RUTA</h4>
        </div>
        <div class="row">
            <div class="col-xs-12 col-sm-7 col-sm-offset-5">
                <div class="parrafo-s1">
                    <p class="p1">Bogotá está formulando la Política Distrital de Movilidad Motorizada de Cero y Bajas Emisiones 2022-2040, que será la hoja de ruta de corto, mediano y largo plazo para consolidar esta movilidad como una alternativa eficiente, accesible y competitiva en la ciudad y sus municipios aledaños.</p>
                    <p class="p1">La Política definirá acciones orientadas a:</p>
                    <ul>
                        <li class="li-1">Diseñar incentivos para la movilidad eléctrica y desincentivos para la movilidad basada en combustibles fósiles.</li>
                        <li class="li-1">Consolidar una red inteligente de infraestructura de recarga.</li>
                        <li class="li-1">Generar un entorno de comunicación, pedagogía y desarrollo de capacidades técnicas para soportar la transición.</li>
                        <li class="li-1">Desarrollar un ecosistema de generación de investigación e innovación, de la mano de los actores de la cadena de valor.</li>
                        <li class="li-1">Promover la economía circular de baterías de vehículos eléctricos.</li>
                    </ul>
                    <br>
                    <p class="p1">La construcción de esta Política consta de 5 fases:</p>
                    <ol>
                        <li class="li-1">Definir el problema a solucionar.</li>
                        <li class="li-1">Construir el diagnóstico detallado de la situación.</li>
                        <li class="li-1">Formular los objetivos, metas y acciones en el corto, mediano y largo plazo.</li>
                        <li class="li-1">Implementar las acciones definidas.</li>
                        <li class="li-1">Evaluar y realizar seguimiento.</li>
                    </ol>
                    <p class="p1">Esta política la construimos entre todos y todas, cada fase ha contado con un proceso de participación que reune a grupos del sector privado, público, académico, sociedad civil, ciudadanía y demás grupos interesados.</p>
                </div>
