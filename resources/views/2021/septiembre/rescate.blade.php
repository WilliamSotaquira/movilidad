                <h3>4. ¿Si el descuento por la amnistía no se ve reflejado en mi volante de pago, qué debo hacer?</h3>
                <hr class="hr-1">
                <p>Si el descuento no le aparece aplicado y la obligación (<strong>Multa</strong> o Acuerdo de Pago que presente mora) está en el marco de la amnistía, el ciudadano podrá presentar solicitud a través de los canales de <a href="https://www.movilidadbogota.gov.co/web/sdqs" rel="noopener noreferrer" target="_blank">PQRS</a> de la Secretaría Distrital de Movilidad, para tal caso, tendrá que rectificar que, conforme al marco del Acuerdo del Rescate Económico y Social de Bogotá, está cobijado por la norma para acceder a la amnistía.</p>
            </div>
            <div class="parrafo">
                <h3>5. ¿Cómo sé si puedo ser beneficiario de la amnistía?</h3>
                <hr class="hr-1">
                <p>Si usted es deudor de obligaciones no tributarias, para este caso: multas por infracciones a las normas de tránsito, <strong>y se encuentra en mora en el pago de éstas o de acuerdos de pago vigentes a partir del el 12 de marzo de 2020</strong>, fecha en que se declaró la emergencia sanitaria en todo el territorio nacional por causa del Covid 19 y hasta la publicación.</p>
                <p>Tenga en cuenta que, las obligaciones que hubiesen surgido a partir de la sanción por conducir bajo el influjo del alcohol o bajo los efectos de sustancias psicoactivas no se concederá el beneficio que trata el Acuerdo 816 frente a estas obligaciones no tributarias.</p>
