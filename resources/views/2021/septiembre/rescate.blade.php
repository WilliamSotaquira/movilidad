            <div class="parrafo">
                <h3>1. ¿Si tengo una multa por infracción a las normas de tránsito me puedo acoger a la amnistía para deudores de obligaciones no tributarias del Acuerdo?</h3>
                <hr class="hr-1">
                <p>Si la multa fue generada durante el periodo comprendido entre el <strong>12 de marzo de 2020</strong> y el <strong>25 de agosto de 2021</strong>, podrá acceder al descuento del 60% del capital y el 100% de los intereses, siempre y cuando cancele el 40% restante del total de la deuda hasta el 15 de diciembre de 2021</p>
                <p>Lo anterior a excepción de las obligaciones que hubiesen surgido a partir de la sanción por conducir bajo el influjo del alcohol o bajo los efectos de sustancias psicoactivas, a las cuales, no se concederá el beneficio que trata el Acuerdo 816 frente a estas obligaciones no tributarias.</p>
            </div>

            <div class="parrafo">
                <h3>2. ¿Si tengo un Acuerdo de Pago en mora, también puedo acogerme a la amnistía para deudores de obligaciones no tributarias del Acuerdo?</h3>
                <hr class="hr-1">
                <p>Los deudores que tengan Acuerdo de Pago vigente y presenten mora en el pago de las mismas, desde el 12 de marzo de 2020 y hasta el 25 de agosto de 2021, también tendrán derecho al descuento en el pago de las obligaciones no tributarias, siempre y cuando paguen el 40% del saldo insoluto de la deuda hasta el 15 de diciembre de 2021.</p>
                <p>Igualmente, lo anterior, a excepción de las facilidades de pago que contengan obligaciones que hubiesen surgido a partir de la sanción por conducir bajo el influjo del alcohol o bajo los efectos de sustancias psicoactivas, a las cuales, no se concederá el beneficio que trata el Acuerdo 816.</p>
                <p>Finalmente, tenga en cuenta que, las facilidades de pago que sean suscritas con posterioridad a la vigencia del Acuerdo 816, no se encontrarán cobijadas con su beneficio, dado que, como ya se informó, únicamente aplica para las facilidades de pago vigentes y que presenten mora en el pago de las mismas, desde el 12 de marzo de 2020 y hasta el 25 de agosto de 2021.</p>
                <p>En ningún caso se podrá acceder al descuento del Acuerdo 816 al integrar sus obligaciones en un nuevo Acuerdo de Pago.</p>
            </div>

            <div class="parrafo">
                <h3>3. ¿Cómo puedo acceder al beneficio de la amnistía?</h3>
                <hr class="hr-1">
                <p>Puede acceder al beneficio ingresando al sitio web <a href="http://www.movilidadbogota.gov.co" rel="noopener noreferrer" target="_blank">www.movilidadbogota.gov.co</a>, link consulta de comparendos, seguidamente, haciendo clic en el botón <a href="https://consultas.transitobogota.gov.co:8010/publico/index3.php" rel="noopener noreferrer" target="_blank">CONSULTA DE COMPARENDOS</a> y finalmente, digitando su número de documento para acceder a la opción de pagos de cada una de sus obligaciones vigentes y que se encuentren en mora con la Secretaría Distrital de Movilidad, bien sea mediante la plataforma PSE o imprimiendo el volante de pago para cancelar en la sucursal bancaria pertinente, para lo cual, tenga en cuenta: utilizar impresora láser, que el volante es válido únicamente por el día que es impreso y que el pago se puede realizar únicamente en los bancos <strong>Occidente</strong> y <strong>Caja Social</strong>, además de los puntos <strong>Éxito</strong>.</p>
                <p>Tenga en cuenta que puede acceder al beneficio de la amnistía, de forma directa y sin necesidad de pedir citas, sin llenar un formulario o sin necesidad de acercarse a un punto físico para solicitar el descuento.</p>
